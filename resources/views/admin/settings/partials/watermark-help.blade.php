                <ul class="text-sm text-purple-800 space-y-1">
                    <li>• <strong>Bottom-right:</strong> Most common, least intrusive</li>
                    <li>• <strong>Center:</strong> Maximum visibility, more intrusive</li>
                    <li>• <strong>Opacity:</strong> 30-60% for subtle branding, 70-90% for strong protection</li>
                    <li>• <strong>Size:</strong> Medium works for most cases, adjust based on your images</li>
                </ul>
