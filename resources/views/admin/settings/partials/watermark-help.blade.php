                <h5 class="font-medium text-blue-900 mb-2">Right-Click Protection</h5>
                <p class="text-sm text-blue-800">Disables the browser's context menu when users right-click on product images, preventing easy access to "Save Image As" option.</p>
            </div>
            
            <div class="bg-blue-50 border border-blue-200 rounded-lg p-4">
                <h5 class="font-medium text-blue-900 mb-2">Drag & Drop Protection</h5>
                <p class="text-sm text-blue-800">Prevents users from dragging images to their desktop or other applications to save them.</p>
            </div>
            
            <div class="bg-blue-50 border border-blue-200 rounded-lg p-4">
                <h5 class="font-medium text-blue-900 mb-2">Keyboard Shortcuts</h5>
                <p class="text-sm text-blue-800">Blocks common keyboard shortcuts like Ctrl+S (save page) and F12 (developer tools) to discourage image extraction.</p>
            </div>
            
            <div class="bg-blue-50 border border-blue-200 rounded-lg p-4">
                <h5 class="font-medium text-blue-900 mb-2">Watermarking</h5>
                <p class="text-sm text-blue-800">Adds your text and/or logo overlay to all product images, making them less useful if stolen and promoting your brand.</p>
            </div>
