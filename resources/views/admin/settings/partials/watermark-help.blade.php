                <h5 class="font-medium text-purple-900 mb-2">Text Watermarks</h5>
                <ul class="text-sm text-purple-800 space-y-1">
                    <li>• Keep text short and readable (recommended: 20 characters or less)</li>
                    <li>• Use your brand name, website URL, or copyright notice</li>
                    <li>• Choose colors that contrast well with your product images</li>
                    <li>• If left empty, your site name will be used automatically</li>
                </ul>
            </div>
            
            <div class="bg-purple-50 border border-purple-200 rounded-lg p-4">
                <h5 class="font-medium text-purple-900 mb-2">Logo Watermarks</h5>
                <ul class="text-sm text-purple-800 space-y-1">
                    <li>• Recommended size: 100x100 pixels or smaller</li>
                    <li>• Use PNG format with transparency for best results</li>
                    <li>• Keep file size under 2MB for optimal performance</li>
                    <li>• Simple, high-contrast logos work best</li>
                </ul>
            </div>
            
            <div class="bg-purple-50 border border-purple-200 rounded-lg p-4">
